# 4 Escribe un programa que solicite una letra al usuario y determine si es una vocal (a, e,
# i, o, u).

v = input('ingrese letra:')

if v == A or v == a:
    print('vocal')
    
elif v == E or v == e:
    print('vocal')
        
elif v == I or v == i :
    print('vocal')
            
elif v == O or v == o :
    print('vocal')
                
elif v == U or v == u :
    print('vocal')
                    
else:
    print('no es vocal')